task3 = Task(
    description='Using the strategy and research, write a 500-word blog post on AI in healthcare.',
    agent=writer,
    depends_on=[task2],
    expected_output='A well-structured blog post of approximately 500 words on AI trends in healthcare.'
)

task4 = Task(
    description='Proofread and edit the blog post for tone, clarity, and grammar.',
    agent=editor,
    depends_on=[task3],
    expected_output='A polished and edited version of the blog post, ready for publication.'
)

# Define the crew and run the workflow
crew = Crew(
    agents=[content_strategist, researcher, writer, editor],
    tasks=[task1, task2, task3, task4],
    verbose=True
)

result = crew.kickoff()
